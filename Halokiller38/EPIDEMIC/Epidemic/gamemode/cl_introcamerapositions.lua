function ccGenerateIntroCamPos( ply, cmd, args )
	
	local pos = ply:EyePos();
	local ang = ply:EyeAngles();
	
	ply:PrintMessage( 2, "\t{\n" );
	ply:PrintMessage( 2, "\t\tPos = Vector( " .. tostring( math.Round( pos.x ) ) .. ", " .. tostring( math.Round( pos.y ) ) .. ", " .. tostring( math.Round( pos.z ) ) .. " ),\n" );
	ply:PrintMessage( 2, "\t\tAng = Angle( " .. tostring( math.Round( ang.p ) ) .. ", " .. tostring( math.Round( ang.y ) ) .. ", 0 ),\n" );
	ply:PrintMessage( 2, "\t},\n" );
	
end
concommand.Add( "rp_dev_gencampos", ccGenerateIntroCamPos );


